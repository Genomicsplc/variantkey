			h := EncodeRefAlt(cdata[i], cdata[j])
			if h != exp[k] {
				t.Errorf("expecting %x, got %#v - REF=%#v - ALT=%#v", exp[k], h, cdata[i], cdata[j])
			}
			ref, alt, sizeref, sizealt, len := DecodeRefAlt(h)
			if len > 0 {
				numrev++
				if sizeref != lenref {
					t.Errorf("LEN REF expecting %#v, got %#v", lenref, sizeref)
				}
				if sizealt != lenalt {
					t.Errorf("LEN ALT expecting %#v, got %#v", lenalt, sizealt)
				}
				if ref != strings.ToUpper(cdata[i]) {
					t.Errorf("REF expecting %#v, got %#v", cdata[i], ref)
				}
				if alt != strings.ToUpper(cdata[j]) {
					t.Errorf("ALT expecting '%#v', got '%#v'", cdata[j], alt)
				}
			}
			k++
			h = EncodeRefAlt(cdata[j], cdata[i])
